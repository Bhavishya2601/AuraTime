export const addWatches = (req, res) =>{
    const {name, img, price} = req.body
    const lastWatch = watch[watch.length - 1];
    const nextId = lastWatch ? lastWatch.id + 1 : 1;
    const newWatch = {
        id: nextId,
        name,
        img,
        price
    }
    watch.push(newWatch)
    res.status(201).json(newWatch)
}